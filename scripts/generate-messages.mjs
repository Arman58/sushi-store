function deepUpdate(d, u) {
  for (const [k, v] of Object.entries(u)) {
    if (v && typeof v === "object" && !Array.isArray(v) && d[k] && typeof d[k] === "object") {
      deepUpdate(d[k], v);
    } else {
      d[k] = v;
    }
  }
}

